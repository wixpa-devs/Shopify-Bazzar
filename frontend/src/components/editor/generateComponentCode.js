const SHOPIFY_THEME_FONT_STACK =
   'var(--font-body-family, -apple-system, BlinkMacSystemFont, "Segoe UI", Roboto, sans-serif)';

const applyThemeFontFamily = (code) => {
   if (typeof code !== "string" || !code.trim()) return code;

   const updatedCode = code.replace(
      /font-family\s*:\s*[^;]+;/gi,
      `font-family: ${SHOPIFY_THEME_FONT_STACK};`,
   );

   if (updatedCode !== code) return updatedCode;

   // If variant does not define a root font-family, inject a safe global fallback.
   if (/<style[\s>]/i.test(updatedCode)) {
      return updatedCode.replace(
         /<style([^>]*)>/i,
         `<style$1>\n  :root, body { font-family: ${SHOPIFY_THEME_FONT_STACK}; }\n`,
      );
   }

   return `<style>:root, body { font-family: ${SHOPIFY_THEME_FONT_STACK}; }</style>\n${updatedCode}`;
};

const applyShopifyDataBindings = (variantId, config, code) => {
   const isCollectionOrProduct =
      variantId.startsWith("collection-slider-") ||
      variantId.startsWith("product-card-slider-");
   const useShopifyData =
      config?.useShopifyData === true ||
      String(config?.useShopifyData || "").toLowerCase() === "true";
   if (!isCollectionOrProduct || !useShopifyData) return code;

   const handle = String(config.collectionHandle || "all").trim();
   const limit = Number(config.productsLimit || 8);
   const safeLimit = Number.isFinite(limit) && limit > 0 ? Math.min(limit, 50) : 8;

   const liquidBindings = `{% assign sbz_collection_handle = "${handle}" %}
{% assign sbz_products_limit = ${safeLimit} %}
{% assign sbz_collection = collections[sbz_collection_handle] %}
{% if sbz_collection == blank %}
  {% assign sbz_collection = collections.all %}
{% endif %}
{% comment %}
  Shopify Bazzar auto data mode:
  - sbz_collection.products => product title, vendor, images, price, compare_at_price, variants, description
  - update this section markup to loop over sbz_collection.products | slice: 0, sbz_products_limit
{% endcomment %}`;

   return `${liquidBindings}\n\n${code}`;
};


